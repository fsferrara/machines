################################################################################
# MODERN UNIX ALIASES
################################################################################

# Bat: A cat clone with syntax highlighting and Git integration.
alias mcat="bat"

# Lsd: A modern replacement for ls with more features and better defaults.
alias mls="lsd"

# Delta: A syntax-highlighting pager for git and diff output.
alias mdiff="delta"

# Dust: A more intuitive version of du in Rust.
alias mdu="dust"

# Duf: A better version of df in Rust.
alias mdf="duf"

# Broot: A new way to see and navigate directory trees.
alias mtree="broot"

# Fd: A simple, fast and user-friendly alternative to find.
alias mfind="fd"

# Ripgrep: A line-oriented search tool that recursively searches the current directory for a regex pattern.
alias mgrep="rg"

# Mcfly: A modern replacement for the command history.
alias mhistory="mcfly"

# Jq: A lightweight and flexible command-line JSON processor.
alias mjson="jq"

# Sd: A find and replace tool that works with regex.
alias msd="sd"

# Glances: A cross-platform system monitoring tool.
alias mtop="glances"

# Gping: A ping tool that shows a graph of the ping times.
alias mping="gping"

# Procs: A modern replacement for ps written in Rust.
alias mps="procs"

# Doggo: Command-line DNS client for humans. dig on steroids
alias mdig="doggo"
